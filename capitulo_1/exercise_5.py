# 5. What is your average speed in miles per hour?

tempo_total_em_segundos = 42 * 60 + 42
milhas = 10 / 1.61
tempo_em_horas = tempo_total_em_segundos / 3600
velocidade_media_em_milhas_por_horas = milhas / tempo_em_horas

print(f"A velocidade média correndo 10 quilômetros em 42 minutos e 42 segundos \
é de {velocidade_media_em_milhas_por_horas:.2f} milhas por horas.")
